    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        // TODO add your handling code here:
        int linhaSelecionada = tbAgencias.getSelectedRow();
        int id = Integer.valueOf((String) tbAgencias.getValueAt(linhaSelecionada, 0));
        
        boolean deletado = Principal.deletarAgencia(id);
        
        if (deletado) {
            JOptionPane.showMessageDialog(this, "Agência " + id + " foi excluída com sucesso.");
            
            atualizarListaAgencias();
        } else {
            JOptionPane.showMessageDialog(this, "Agência " + id + " não foi excluída. Tente novamente.");
        }
    }//GEN-LAST:event_btExcluirActionPerformed

