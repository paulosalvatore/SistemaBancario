    private void atualizarListaAgencias() {
        System.out.println("Atualizar Lista de Agências.");
        
        DefaultTableModel modelo = new DefaultTableModel();
        
        modelo.addColumn("ID");
        modelo.addColumn("Número");
        modelo.addColumn("Nome");
        
        ResultSet agencias = Principal.listarAgencias();
        
        try {
            while (agencias.next()) {                
                int id = agencias.getInt("id");
                String numero = agencias.getString("numero");
                String nome = agencias.getString("nome");
                
                String[] dadosAgencia = new String[]{
                    Integer.toString(id),
                    numero,
                    nome
                };
                
                modelo.addRow(dadosAgencia);
            }
        } catch (Exception e) {
            System.out.println(e);
        }
        
        if (modelo.getRowCount() == 0) {
            modelo.addRow(
                new String[]{
                    "Nenhuma agência encontrada.",
                    null,
                    null
                }
            );
            
            modelo.setColumnCount(1);
        }
        
        tbAgencias.setModel(modelo);
    }

